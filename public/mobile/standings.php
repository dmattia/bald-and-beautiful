          <h1>RESIDENCE HALL COMPETITION</h1>
                  <p>Over the first years of The Bald & The Beautiful event participation among residence halls has varied greatly.  This year, we challenge more dorms to get involved.  The hall that gets the most guys to shave their head will receive funds to get food for the dorm. If you would like to schedule a time slot for your dorm to shave together, email Cullen at cmcnamee@nd.edu.  Additionally, to track pre-registration totals or see last year's totals by dorm, see <a href="mens_rankings.php">MEN'S HALL RANKINGS</a>    </p>

                  <p>Ladies who pre-register to donate eight or more inches of hair through Pantene Beautiful Lengths will be entered into a raffle for a basket of hair products.  Also, Notre Dame's own University Hair Stylists (located in the LaFortune Basement) will be offering a discount to participants who would like their new hairdo styled.  To track the inches of hair donated by members of your hall, see <a href="womens_rankings.php">WOMEN'S HALL RANKINGS</a>    </p>



